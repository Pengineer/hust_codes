package csdc.bean;

import java.util.Date;


/**
 * 变更申报数据读取接口类
 *
 */
public class SyncStatus {
	
	private String id;//主键
	private String peer;//数据交互对象
	private String interfaceName;//数据传输接口
	private String projectId;//smdb申报id
	private String midinspectionId;//smdb中检id
	private String variationId;//smdb变更id
	private String endinspectionId;//smdb结项id
	private Date requestDate;//请求日期
	private Account requestAccount;
	private String requestAccountBelong;//请求账号所属
	private Date replyDate;//响应日期 
	private Account responseAccount;
	private String responseAccountBelong;//响应账号所属
	private Integer status;//数据同步状态[0：默认；1：失败；2：成功]
	private String note;//备注
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPeer() {
		return peer;
	}
	public void setPeer(String peer) {
		this.peer = peer;
	}
	public String getInterfaceName() {
		return interfaceName;
	}
	public void setInterfaceName(String interfaceName) {
		this.interfaceName = interfaceName;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getMidinspectionId() {
		return midinspectionId;
	}
	public void setMidinspectionId(String midinspectionId) {
		this.midinspectionId = midinspectionId;
	}
	public String getVariationId() {
		return variationId;
	}
	public void setVariationId(String variationId) {
		this.variationId = variationId;
	}
	public String getEndinspectionId() {
		return endinspectionId;
	}
	public void setEndinspectionId(String endinspectionId) {
		this.endinspectionId = endinspectionId;
	}
	public Date getRequestDate() {
		return requestDate;
	}
	public void setRequestDate(Date requestDate) {
		this.requestDate = requestDate;
	}
	public Account getRequestAccount() {
		return requestAccount;
	}
	public void setRequestAccount(Account requestAccount) {
		this.requestAccount = requestAccount;
	}
	public String getRequestAccountBelong() {
		return requestAccountBelong;
	}
	public void setRequestAccountBelong(String requestAccountBelong) {
		this.requestAccountBelong = requestAccountBelong;
	}
	public Date getReplyDate() {
		return replyDate;
	}
	public void setReplyDate(Date replyDate) {
		this.replyDate = replyDate;
	}
	public Account getResponseAccount() {
		return responseAccount;
	}
	public void setResponseAccount(Account responseAccount) {
		this.responseAccount = responseAccount;
	}
	public String getResponseAccountBelong() {
		return responseAccountBelong;
	}
	public void setResponseAccountBelong(String responseAccountBelong) {
		this.responseAccountBelong = responseAccountBelong;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}

	
}
