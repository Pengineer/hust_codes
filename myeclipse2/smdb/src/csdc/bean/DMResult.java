package csdc.bean;

import java.util.Date;

/**
 * 数据挖掘结果类
 * @author fengcl
 */
public class DMResult implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	
	private String id;
	private String title;		//挖掘标题
	private String description;	//挖掘描述
	private String type;		//挖掘类别
	private Date date;			//挖掘时间
	private String config; 		//挖掘配置
	private String resultJson; 	//挖掘结果json
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getConfig() {
		return config;
	}
	public void setConfig(String config) {
		this.config = config;
	}
	public String getResultJson() {
		return resultJson;
	}
	public void setResultJson(String resultJson) {
		this.resultJson = resultJson;
	}
}
