package org.csdc.bean;

import java.util.HashMap;
import java.util.Map;
/**
 * 通用JSON数据结构
 * @author jintf
 * @date 2014-6-15
 */
public class JsonData {
	public boolean success=true;  //请求是否达到预期结果
	public Map data = new HashMap();  //返回数据信息
	public Map errors = new HashMap(); //返回错误信息
	public String msg; //返回的字符串消息，对返回结果的整体描述
	
	public JsonData(){
		
	}
	
	public JsonData(boolean success){
		this.success = success;
	}
}
