package csdc.action.pop.select;

/**
 * 弹层基类
 * @author 龚凡
 */
public abstract class BaseAction extends csdc.action.BaseAction {

	private static final long serialVersionUID = 1L;
	protected int label;// 是否账号模块调用(0其它,1账号,2教师账号注册)

	public int getLabel() {
		return label;
	}
	public void setLabel(int label) {
		this.label = label;
	}

}
