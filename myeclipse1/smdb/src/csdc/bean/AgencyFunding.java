package csdc.bean;

/**
 * 
 * @author maowh
 *
 */
public class AgencyFunding {
	public String id;//id
	private String agencyName;//收款机构名称
	private Agency agency;//收款机构id（FK）
	private Double fee;//经费额
	private Integer count;//拨款条目数
	private FundingBatch fundingBatch;//拨款批次（FK）
	private BankAccount bankAccount;//银行账号信息（FK）
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAgencyName() {
		return agencyName;
	}
	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}
	public Agency getAgency() {
		return agency;
	}
	public void setAgency(Agency agency) {
		this.agency = agency;
	}
	public Double getFee() {
		return fee;
	}
	public void setFee(Double fee) {
		this.fee = fee;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public FundingBatch getFundingBatch() {
		return fundingBatch;
	}
	public void setFundingBatch(FundingBatch fundingBatch) {
		this.fundingBatch = fundingBatch;
	}
	public BankAccount getBankAccount() {
		return bankAccount;
	}
	public void setBankAccount(BankAccount bankAccount) {
		this.bankAccount = bankAccount;
	}
	
}
