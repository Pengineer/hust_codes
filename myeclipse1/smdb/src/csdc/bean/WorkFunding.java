package csdc.bean;

import java.util.Date;

public class WorkFunding implements java.io.Serializable {
	private static final long serialVersionUID = -7831202384322603052L;
	
	private String id;
	private String name;
	private Double fee;
	private String note;
	private String type;
	private Date date;
	private FundingList fundingList;
	private String agencyName;//收款机构名称
	private String payee;//收款人姓名或收款单位名称
	private Person person;//收款人
	private Agency agency;//收款机构
	private Institute institute;//收款研究机构
	private Department department;//收款院系
	private AgencyFunding agencyFunding;//机构经费
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getFee() {
		return fee;
	}
	public void setFee(Double fee) {
		this.fee = fee;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public FundingList getFundingList() {
		return fundingList;
	}
	public void setFundingList(FundingList fundingList) {
		this.fundingList = fundingList;
	}
	public String getAgencyName() {
		return agencyName;
	}
	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}
	public String getPayee() {
		return payee;
	}
	public void setPayee(String payee) {
		this.payee = payee;
	}
	public Person getPerson() {
		return person;
	}
	public void setPerson(Person person) {
		this.person = person;
	}
	public Agency getAgency() {
		return agency;
	}
	public void setAgency(Agency agency) {
		this.agency = agency;
	}
	public Institute getInstitute() {
		return institute;
	}
	public void setInstitute(Institute institute) {
		this.institute = institute;
	}
	public Department getDepartment() {
		return department;
	}
	public void setDepartment(Department department) {
		this.department = department;
	}
	public AgencyFunding getAgencyFunding() {
		return agencyFunding;
	}
	public void setAgencyFunding(AgencyFunding agencyFunding) {
		this.agencyFunding = agencyFunding;
	}
}
