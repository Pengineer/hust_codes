package csdc.bean;

import java.util.Set;

public class SysoptionValue {

	private String id;// 记录的ID
	private String value;// 下拉框的值
	private Sysoption sysoption;// 外键对象
	private Set<Person> ethnic;// 民族
	private Set<Person> membership;// 政治面貌
	private Set<Person> idcardtype;// 证件类型
	private Set<Person> sex;// 性别

	//---get && set---
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public Sysoption getSysoption() {
		return sysoption;
	}

	public void setSysoption(Sysoption sysoption) {
		this.sysoption = sysoption;
	}

	public Set<Person> getEthnic() {
		return ethnic;
	}

	public void setEthnic(Set<Person> ethnic) {
		this.ethnic = ethnic;
	}

	public Set<Person> getMembership() {
		return membership;
	}

	public void setMembership(Set<Person> membership) {
		this.membership = membership;
	}

	public Set<Person> getIdcardtype() {
		return idcardtype;
	}

	public void setIdcardtype(Set<Person> idcardtype) {
		this.idcardtype = idcardtype;
	}

	public Set<Person> getSex() {
		return sex;
	}

	public void setSex(Set<Person> sex) {
		this.sex = sex;
	}
}